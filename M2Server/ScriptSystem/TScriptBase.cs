        /// 取检测命令对应编号
        /// </summary>
        /// <param name="sCmd"></param>
        /// <returns></returns>
        public int GetQuestConditionIndex(string sCmd)
        {
            var query = from d in ScriptDictionary where d.Key == sCmd select d.Value;
            return query.FirstOrDefault();
